package com.spider.manager.model;

public class SupAndTtgModel {

    private final String sup;

    private final String ttg;

    public SupAndTtgModel(String sup, String ttg) {

        this.sup = sup;
        this.ttg = ttg;
    }

    public String getSup() {

        return sup;
    }

    public String getTtg() {

        return ttg;
    }
}
